	public void print(Tile[][] tab) {
		for(int i=0; i<this.height; i++) {
			for(int j=0; j<this.width; j++) {
				tab[this.p1.y+i][this.p1.x+j] = TileFactory.getInstance().getTileAt(map[i][j]);
			}
		}
	}

	public int getStairDownX() { return this.stairDown.x; }
	public int getStairDownY() { return this.stairDown.y; }

